package com.example.inreal;

public class CategoriesDrinksAndFoods {
    private String name;
    private String description;
    private int imageId;

    public CategoriesDrinksAndFoods(String name, String description, int imageId) {
        this.name = name;
        this.description = description;
        this.imageId = imageId;
    }

    public int getImageId() {
        return imageId;
    }

    public String getDescription() {
        return description;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return name;
    }
}
